# Print a string
print("Hello, world!")

# Print multiple values
x = 10
y = 20
print("The value of x is:", x, "and the value of y is:", y)

# Print using formatted strings (Python 3.6+)
name = "Alice"
age = 30
print(f"My name is {name} and I am {age} years old.")

# Printing with concatenation
a = 5
b = 7
print("The sum of " + str(a) + " and " + str(b) + " is " + str(a + b))

# Printing with end parameter (to specify the end character)
print("Hello", end=" ")
print("world!")

# Printing without newline
print("This is", end=" ")
print("on the same line.")

# Printing with sep parameter (to specify the separator)
print("apple", "banana", "cherry", sep=", ")

# Printing numbers with different bases
number = 42
print("Binary:", bin(number))
print("Octal:", oct(number))
print("Hexadecimal:", hex(number))

# Printing with precision for floating point numbers
pi = 3.141592653589793238
print(f"Value of pi (rounded to 2 decimal places): {pi:.2f}")

# Printing a range of numbers
print("Numbers from 1 to 5:")
for i in range(1, 6):
    print(i)

# Printing a list
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Printing a dictionary
person = {"name": "John", "age": 30, "city": "New York"}
print("Person details:", person)

# Printing using string formatting (older method)
name = "Bob"
age = 25
print("My name is %s and I am %d years old." % (name, age))

# Printing with repetition
print("Ha" * 5)


